console.log("Loops tutorial");

// for loop
console.log("For loop");
let a =1;
for(let i=0; i<10; i++){
    console.log(a+i);
}

// while loop
console.log("while loop");
let j=1;
while(j<10){
    console.log(j);
    j++;
}

// do while loop
console.log("Do-While loop");
let k=1;
do {
console.log(k);
k++;
} while(k<10)